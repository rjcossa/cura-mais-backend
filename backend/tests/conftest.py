async def get_outbox_param(session_factory, destination: str, event_type: str, param_key: str) -> str | None:
    """Test helper: pulls a raw secret (verification token / OTP code) out
    of the outbox, standing in for "the user reading the email/SMS they
    received" since these tests don't have a real inbox to check.
    """
    from sqlalchemy import select

    async with session_factory() as session:
        stmt = (
            select(EventOutbox)
            .where(EventOutbox.event_type == event_type)
            .order_by(EventOutbox.created_at.desc())
        )
        rows = (await session.execute(stmt)).scalars().all()
        for row in rows:
            payload = row.payload or {}
            if payload.get("destination") == destination:
                return payload.get("parameters", {}).get(param_key)
    return None


