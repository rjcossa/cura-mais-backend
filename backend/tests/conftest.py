async def fill_doctor_application_sections(client, headers: dict) -> None:
    for section, payload in DOCTOR_SECTION_PAYLOADS.items():
        r = await client.put(f"/api/v1/onboarding/me/application/sections/{section}", json=payload, headers=headers)
        assert r.status_code == 200, r.text


async def upload_doctor_documents(client, headers: dict) -> None:
    for document_type in DOCTOR_MANDATORY_DOCUMENTS:
        mime = "image/jpeg" if document_type == "PROFILE_PHOTO" else "application/pdf"
        r = await client.post(
            "/api/v1/onboarding/me/application/documents/upload-request",
            json={"documentType": document_type, "fileName": f"{document_type}.pdf", "mimeType": mime, "fileSize": 1000},
            headers=headers,
        )
        assert r.status_code == 200, r.text
        application_document_id = r.json()["data"]["applicationDocumentId"]
        r2 = await client.post(
            f"/api/v1/onboarding/me/application/documents/{application_document_id}/confirm",
            json={
                "checksum": "abc123",
                "documentNumber": "DOC-1",
                "issuingAuthority": "Authority",
                "issuingCountry": "MZ",
                "issueDate": "2020-01-01",
                "expiryDate": "2030-01-01",
            },
            headers=headers,
        )
        assert r2.status_code == 200, r2.text


async def create_complete_doctor_application(client, applicant_token: str) -> dict:
    """Creates a fully-complete-but-not-yet-submitted DOCTOR application
    for the given applicant token. Returns the application summary dict.
    """
    headers = auth_header(applicant_token)
    r = await client.post(
        "/api/v1/onboarding/me/application", json={"applicantType": "DOCTOR", "fullName": "Ana Mabote"}, headers=headers
    )
    assert r.status_code == 200, r.text
    await fill_doctor_application_sections(client, headers)
    await upload_doctor_documents(client, headers)
    return r.json()["data"]
