# --- Onboarding test helpers -----------------------------------------------
#
# Onboarding tests need users with specific Identity roles/permissions
# (reviewers, approvers) without going through the full registration/MFA
# flow — these helpers create a user directly and mint a token for them,
# the same shortcut Identity's own admin/role tests use.

_ONBOARDING_COUNTER = {"n": 0}


async def make_user_with_role(session_factory, role_code: str, *, email: str | None = None, mobile: str | None = None):
    """Creates an ACTIVE, verified user with one active role. Returns the
    user's id.
    """
    from sqlalchemy import select

    from app.modules.identity.domain.models import Role, User, UserRole

    _ONBOARDING_COUNTER["n"] += 1
    n = _ONBOARDING_COUNTER["n"]
    email = email or f"onboarding-test-{n}@example.com"
    mobile = mobile or f"+2588{40000000 + n}"

    async with session_factory() as session:
        user = User(
            email=email,
            mobile_number=mobile,
            password_hash="x",
            account_status="ACTIVE",
            email_verified=True,
            mobile_verified=True,
        )
        session.add(user)
        await session.flush()
        role = (await session.execute(select(Role).where(Role.code == role_code))).scalar_one()
        session.add(UserRole(user_id=user.id, role_id=role.id, active=True))
        await session.commit()
        return user.id


async def token_for(user_id, roles: list[str], permissions: list[str]) -> str:
    from app.core.config import get_settings
    from app.modules.identity.application.tokens import AccessTokenCodec

    codec = AccessTokenCodec(get_settings())
    token, _ = codec.build_and_sign(
        user_id=user_id,
        session_id=user_id,
        roles=roles,
        permissions=permissions,
        email_verified=True,
        mobile_verified=True,
        mfa_verified=False,
        token_version=1,
    )
    return token


def auth_header(token: str) -> dict:
    return {"Authorization": f"Bearer {token}"}


